package org.ruchith.ae.peer;

import it.unisa.dia.gas.jpbc.Element;

public class Contact {

	private String name;
	private Element id;
	private Element r;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Element getId() {
		return id;
	}
	public void setId(Element id) {
		this.id = id;
	}
	public Element getR() {
		return r;
	}
	public void setR(Element r) {
		this.r = r;
	}
	
	
}
